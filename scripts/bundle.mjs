// Bundles module folders into the single files the app actually loads. We ship ONE
// concatenated file per side so top-level const/let stay in one shared scope (separate
// <script> tags / require() modules do NOT share them). Edit the modules under
// src/mod/ (renderer) or main-mod/ (main process), then `npm run bundle` (runs
// automatically before start/dev/check/dist).
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
const root = join(dirname(fileURLToPath(import.meta.url)), '..');
function bundle(modDir, outFile, label) {
  const dir = join(root, modDir);
  let files;
  try { files = readdirSync(dir).filter((f) => f.endsWith('.js')).sort(); }
  catch { console.log(`(skip ${label}: no ${modDir})`); return; }
  if (!files.length) return;
  const header = `// AUTO-GENERATED by scripts/bundle.mjs from ${modDir}/*.js — DO NOT EDIT DIRECTLY.\n`
    + `// Edit the module under ${modDir}/ then run \`npm run bundle\`.\n`;
  const body = files.map((f) => readFileSync(join(dir, f), 'utf8')).join('');
  writeFileSync(join(root, outFile), header + body);
  console.log(`bundled ${files.length} modules -> ${outFile} (${(header + body).length} bytes)`);
}
bundle('src/mod', 'src/renderer.js', 'renderer');
bundle('main-mod', 'main.js', 'main');
