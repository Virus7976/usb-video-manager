// Bundles src/mod/*.js (in filename order) into src/renderer.js — the single script the
// app loads. We ship ONE concatenated file so top-level const/let stay in one shared
// scope (separate <script> tags do NOT share const/let). Edit the modules, then run
// `npm run bundle` (also runs automatically before dist/dev).
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const modDir = join(root, 'src', 'mod');
const files = readdirSync(modDir).filter((f) => f.endsWith('.js')).sort();
const header = '// AUTO-GENERATED by scripts/bundle.mjs from src/mod/*.js — DO NOT EDIT DIRECTLY.\n'
  + '// Edit the module under src/mod/ then run `npm run bundle`.\n';
const body = files.map((f) => readFileSync(join(modDir, f), 'utf8')).join('');
writeFileSync(join(root, 'src', 'renderer.js'), header + body);
console.log(`bundled ${files.length} modules -> src/renderer.js (${(header + body).length} bytes)`);
